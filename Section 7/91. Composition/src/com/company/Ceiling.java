package com.company;

public class Ceiling {
    private int height;
    private int paintColour;

    public Ceiling(int height, int paintColour) {
        this.height = height;
        this.paintColour = paintColour;
    }

    public int getHeight() {
        return height;
    }

    public int getPaintColour() {
        return paintColour;
    }
}
